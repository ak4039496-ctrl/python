# Declare variables
a = 6       # binary: 110
b = 3       # binary: 011

# Bitwise AND (&)
print("a & b :-", a & b)

# Bitwise OR (|)
print("a | b :-", a | b)

# Bitwise XOR (^)
print("a ^ b :-", a ^ b)

# Bitwise NOT (~)
print("~a :-", ~a)

# Left shift (<<)
print("a << 1 :-", a << 1)

# Right shift (>>)
print("a >> 1 :-", a >> 1)