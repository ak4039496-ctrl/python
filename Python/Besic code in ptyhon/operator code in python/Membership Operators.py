# Description: Demonstration of Python membership operators

nums = [1, 2, 3, 4]

# Membership Operators
print("2 in nums      ->", 2 in nums)       # True
print("5 not in nums ->", 5 not in nums)   # True 
print("'a' in 'amit'  ->", 'a' in 'amit')   # True 
print("'a' in 'Amit' ->",'a' in 'Amit')    #False
